const getAmount = function () {
  const totalAmount =
    services['Living Room Areas'].cost +
    services['Kitchen'].cost +
    this.bath_rooms * services['Bedrooms'].cost +
    this.bath_rooms * services['Bathrooms'].cost +
    (this.laundry_and_ironing === true
      ? services['Laundry & Ironing'].cost
      : 0) +
    (this.oven === true ? services['Oven'].cost : 0) +
    (this.fridge === true ? services['Fridge'].cost : 0) +
    (this.cabinets === true ? services['Cabinets'].cost : 0);
  return Number(totalAmount);
};

